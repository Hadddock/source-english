export const TextIcon: React.FC<{
  text: string;
  size?: number;
  textsize?: number;
  fontSize?: number;
  pronunciation?: string;
  style?: any;
}> = ({ text, size = 32, fontSize, textsize, pronunciation, style }) => (
  <View
    style={[
      {
        width: size,
        height: size,
        borderRadius: 6,
        alignItems: 'center',
        justifyContent: 'center',
        overflow: 'hidden',
        // Remove margin/padding here
      },
      style,
    ]}
  >
    <ReadableText
      text={text}
      pronunciation={pronunciation}
      style={{
        fontSize:
          (textsize ? textsize : 25) *
          (text && text.length > 5
            ? 0.6
            : text && text.length > 3
            ? 0.8
            : text && text.length > 2
            ? 0.85
            : 1),
        color: '#333',
        fontWeight: '500',
        textAlign: 'center',
        fontFamily: 'Lexend_400Regular',
        marginBottom: 0,
        padding: 0,
        // Remove margin/padding here as well
      }}
      numberOfLines={1}
      ellipsizeMode="tail"
      displayText={text}
    />
  </View>
);

export const GenericTextIcon: React.FC<{ word: string; size?: number }> = ({
  word,
  size = 28,
}) => (
  <View
    style={{
      width: size,
      height: Math.max(42, size),

      borderRadius: size / 2,
      // backgroundColor: '#f0f0f0',
      alignItems: 'center',
      justifyContent: 'center',
      overflow: 'hidden',
    }}
  >
    <Text
      style={{
        fontSize: size * 0.85,
        color: '#333',
        fontWeight: '600',
        textAlign: 'center',
        fontFamily: 'Lexend', // Use Lexend font
      }}
      selectable={false}
    >
      {word.charAt(0).toUpperCase()}
    </Text>
  </View>
);

